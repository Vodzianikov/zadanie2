let age = prompt('число от 1 до 7');


switch (age) {
    case 1:
        console.log('Понедельник');
        break;

    case 2:
        console.log('Вторник');
        break;

    case 3:
        console.log('Среда');
        break;

    case 4:
        console.log('Четверг');
        break;

    case 5:
        console.log('Пятница');
        break;

    case 6:
        console.log('Выходной');
        break;

    case 7:
        console.log('Выходной');
        break;
        
    default:
        console.log('error');
        break;
}